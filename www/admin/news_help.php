<p>На поточний час підтримаються стрічки новин:
<ul>
  <li><b>Головна</b> - основна стрічка головної сторінки сайту (ліва частина);</li>
  <li><b>Головна - Оголошення</b> - стрічка оголошень головної сторінки сайту (права частина);</li>
  <li><b>Лабораторія - Таблиці якості води</b> - відповідає одноіменному пункту меню;</li>
  <li><b>Лабораторія - інші відомості</b> - відповідає одноіменному пункту меню;</li>
</ul>
