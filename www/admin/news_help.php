<p>При роботі з стрічками потрібно враховувати слідуючі особливості:
  <ul>
    <li><b>Дата публікації</b> - згідно неї сортується стрічка - більш свіжи публікації зверху;</li>
    <li><b>Дата закінчення</b> - для стрічки <b>Головна - Оголошення</b> - якщо поточна дата є більшою ціеї дати, оголошення потрапляє з головноі сторінки на сторінку <b>Архів оголошень</b>. Для остальних стрічок якщо поточна дата є більшою ціеї дати, новина перестає показуватись на сайті;</li>
    <li><b>Заголовок</b> - використовується для стрічки <b>Головна - Оголошення</b> - якщо <b>Опис</b> недоречно використовувати на головної сторінці;</li>
    <li><b>Опис</b> - використовуеться у випадках коли <b>Заголовок</b> не використовуемо (т.т. в більшості випадків);</li>
    <li><b>Головна новина</b> - помічае новину, яка є головною для стрічки - в цьому випадку вона розміщаеться первою в стрічці на жовтому фоні;</li>
    <li><b>Готовий файл</b> - позволяе завантажити файли Word, Excel; AcrobatReader, які будуть відображатися за допомогою сервіса GoogleDocs;</li>
  </ul>
